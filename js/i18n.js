function translatePage(lang) {
  const dict = translations[lang];
  if (!dict) return;
  
  document.documentElement.lang = lang;

  // Translate data-i18n items
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });

  // Translate placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key]) {
      el.placeholder = dict[key];
    }
  });

  // Keep active state on correct language buttons
  const langButtons = document.querySelectorAll('.navbar__lang-btn');
  langButtons.forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

// Global exposure
window.translatePage = translatePage;
window.translations = translations;

// Automatically execute on script load
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('gz-empire-lang') || 'fr';
  translatePage(savedLang);

  // Monitor clicks on language buttons globally
  document.body.addEventListener('click', (e) => {
    const btn = e.target.closest('.navbar__lang-btn');
    if (btn) {
      const lang = btn.dataset.lang;
      localStorage.setItem('gz-empire-lang', lang);
      translatePage(lang);
    }
  });
});

